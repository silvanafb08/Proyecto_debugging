#ifndef CIENTIFICA_H
#define CIENTIFICA_H

#include <stdio.h>
#include <math.h>
float exponente10(float x) {
    return pow(10, x);
}
float exponentes(float x,float n) {
    return pow(x, n);
}
float exponente2(float x) {
    return pow(x, 2);
}

float raiz(float x) {
    return sqrt(x);
}


#endif // CIENTIFICA_H
